library(ggplot2)

build.df <- function(v) {
    buf <- gsub("\\[\\d+\\]\\s=\\s", "", v);
    buf <- gsub("\\{", "data.frame(", buf);
    code <- paste0("list(", gsub("\\}", ")", buf), ")");
    list.data <- eval(parse(text = code))
    df <- do.call(rbind, list.data)
    df$y <- -df$y
    df
}

